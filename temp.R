    ## **What is the data?**
    
    This data is survey data collected at Utah Valley University in the Spring and Fall 2020 semesters. The survey explores different aspects of science motivation, with a goal of understanding what things may have an effect on a student's science motivation. The questions were adapted from other surveys found in the literature review conducted. Different aspects of demographics were measured, as well. These are the different aspects of science motivation that we explored:
<br>

### Science Identity

Questions regarding "Science Identity" included:
<br>

  <ul>
    <li>"I see myself as a biology person"</li>
    <li>"I see myself as a science person"</li>
    <li>"I see myself as a scientist"</li>
    <li>"Other people (friends/family) see me as a science person"</li>
    <li>"My science instructors see me as a science person"</li>
  </ul>
Questions were measured on a 5 point scale, from "Strongly Disagree" to "Strongly Agree".

